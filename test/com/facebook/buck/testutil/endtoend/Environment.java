package com.facebook.buck.testutil.endtoend;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * {@link Environment} marks static methods in test classes that use the {@link EndToEndRunner} to
 * public static methods that provide {@link EndToEndEnvironment}s to be used as the default
 * environment for all tests in that class
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.METHOD)
public @interface Environment {}
